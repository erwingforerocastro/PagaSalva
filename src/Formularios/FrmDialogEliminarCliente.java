    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        boolean f=FrmInicio.objSistema.quitarCliente(this.txtDocumento.getText());
        if(f){
            try {
                ObjectOutputStream fileOut;
                fileOut=new ObjectOutputStream(new FileOutputStream("infoClientes.dat"));
                fileOut.writeObject(FrmInicio.objSistema);
                fileOut.close();
                JOptionPane.showMessageDialog(this,"El cliente fue quitado con exito");
            } catch (IOException e) {
                System.out.println("Error: "+e.getMessage());
                JOptionPane.showMessageDialog(this,"¡Error al guardar la informacion en el archivo!");
            }
        }else{
            JOptionPane.showMessageDialog(this, "¡¡¡Se generó un error al quitar el cliente!!!");
        }
        this.dispose();

    }//GEN-LAST:event_btnGuardarActionPerformed

