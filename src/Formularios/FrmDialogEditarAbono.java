    private void btnGuardar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardar3ActionPerformed
        String nro=this.txtnro.getText();
        String fecha=this.txtFecha.getText();
        double abono=Double.parseDouble( this.txtAbono.getText());

        FrmInicio.objSistema.editarAbono(FrmInicio.idCliente,FrmInicio.noC, abono, nro, fecha);
        
        try {
            

            ObjectOutputStream fileOut;
            fileOut=new ObjectOutputStream(new FileOutputStream("infoAbonos.dat"));
            fileOut.writeObject(FrmInicio.objSistema);
            fileOut.close();
            JOptionPane.showMessageDialog(this,"¡ La informacion fue guardada con exito !");
        } catch (IOException e) {
            System.out.println("Error:"+e.getMessage());
            JOptionPane.showMessageDialog(this,"¡Error al guardar la informacion !");
        }
        this.dispose();
    }//GEN-LAST:event_btnGuardar3ActionPerformed

