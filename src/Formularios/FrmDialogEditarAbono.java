    private void BtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarActionPerformed
             if(!this.txtnro.getText().equals("")){
                 Abono objAbono=FrmInicio.objSistema.mostrarAbono(FrmInicio.idCliente,FrmInicio.noC,this.txtnro.getText());
            this.txtAbono.setText(Double.toString( objAbono.getAbono()));
            this.txtFecha.setText(objAbono.getFecha());
            this.txtsaldo.setText(Double.toString(objAbono.getSaldo()));
                
            
            if(!this.txtnro.getText().equals(objAbono.getCodigoA())){
                JOptionPane.showMessageDialog(this,"El abono no se encuentra");
            }
        }else{
            JOptionPane.showMessageDialog(this,"Por favor, digite el documento");
        }
    }//GEN-LAST:event_BtnBuscarActionPerformed

